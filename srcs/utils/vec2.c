t_vec		vec_scalar(t_vec vec, t_float a)
{
	t_vec	ret;

	ret[0] = vec[0] * a;
	ret[1] = vec[1] * a;
	ret[2] = vec[2] * a;
	return (ret);
}

t_vec		vec_opposite(t_vec a)
{
	return (vec_scalar(a, -1.0));
}
